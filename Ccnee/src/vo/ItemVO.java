package vo;

public class ItemVO {
	private int itemNo;
	private String itemName;
	private String itemContent;
	private int itemPrice;
	private String itemActivate;
	private String itemFileName;
	private String itemStreCours;
	
	public int getItemNo() {
		return itemNo;
	}
	public void setItemNo(int itemNo) {
		this.itemNo = itemNo;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemContent() {
		return itemContent;
	}
	public void setItemContent(String itemContent) {
		this.itemContent = itemContent;
	}
	public int getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(int itemPrice) {
		this.itemPrice = itemPrice;
	}
	public String getItemActivate() {
		return itemActivate;
	}
	public void setItemActivate(String itemActivate2) {
		this.itemActivate = itemActivate2;
	}
	public String getItemFileName() {
		return itemFileName;
	}
	public void setItemFileName(String itemFileName) {
		this.itemFileName = itemFileName;
	}
	public String getItemStreCours() {
		return itemStreCours;
	}
	public void setItemStreCours(String itemStreCours) {
		this.itemStreCours = itemStreCours;
	}
	
}
